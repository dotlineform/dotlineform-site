function nodeById(documentRef, id) {
  return documentRef.getElementById(id);
}

export function managementShellMount(root) {
  if (!root) return null;
  return root.querySelector("[data-docs-viewer-management-shell-mount]");
}

export function renderDocsViewerManagementShell(options) {
  var settings = options || {};
  var documentRef = settings.document || document;
  var mount = settings.mount;
  if (!mount) return null;

  mount.replaceChildren();

  var template = documentRef.createElement("template");
  template.innerHTML = MANAGEMENT_SHELL_MARKUP;
  mount.appendChild(template.content);
  return findDocsViewerManagementShellRefs({
    document: documentRef,
    root: settings.root || null
  });
}

