from __future__ import annotations

import shutil
from dataclasses import dataclass
from pathlib import Path

from .config import PublicSiteConfig
from .render import render_initial_page


@dataclass(frozen=True)
class BuildResult:
    destination: Path
    copied_count: int
    rendered_count: int


def build_site(repo_root: Path, destination: Path, config: PublicSiteConfig) -> BuildResult:
    destination = destination.resolve()
    _prepare_destination(repo_root.resolve(), destination, config.marker_file)
    _write_marker_files(destination, config)
    copied_count = _copy_root_artifacts(repo_root, destination, config)
    rendered_count = _render_initial_pages(destination, config)
    return BuildResult(
        destination=destination,
        copied_count=copied_count,
        rendered_count=rendered_count,
    )


def _prepare_destination(repo_root: Path, destination: Path, marker_file: str) -> None:
    if destination == repo_root:
        raise RuntimeError("refusing to use repo root as public-site destination")

    if destination.exists():
        marker_path = destination / marker_file
        if marker_path.is_file():
            shutil.rmtree(destination)
        elif any(destination.iterdir()):
            raise RuntimeError(
                f"refusing to replace non-artifact destination without {marker_file}: {destination}"
            )
        else:
            destination.rmdir()

    destination.mkdir(parents=True, exist_ok=False)


def _write_marker_files(destination: Path, config: PublicSiteConfig) -> None:
    (destination / config.marker_file).write_text(
        "Generated by public-site/build/build_site.py.\n",
        encoding="utf-8",
    )
    (destination / config.nojekyll_file).write_text("", encoding="utf-8")


def _copy_root_artifacts(repo_root: Path, destination: Path, config: PublicSiteConfig) -> int:
    copied_count = 0
    for relative in config.root_artifacts:
        source = repo_root / relative
        if not source.is_file():
            raise FileNotFoundError(f"missing root artifact: {relative}")
        target = destination / relative
        target.parent.mkdir(parents=True, exist_ok=True)
        shutil.copy2(source, target)
        copied_count += 1
    return copied_count


def _render_initial_pages(destination: Path, config: PublicSiteConfig) -> int:
    rendered_count = 0
    for relative_path, page in config.initial_pages.items():
        target = destination / relative_path
        target.parent.mkdir(parents=True, exist_ok=True)
        target.write_text(render_initial_page(config, page), encoding="utf-8")
        rendered_count += 1
    return rendered_count
